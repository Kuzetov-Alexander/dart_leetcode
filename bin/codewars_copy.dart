List<int> arrayLeaders(List<int> numbers) {
  List<int> leader = [];

  for (int i = 0; i <= numbers.length - 1; i++) {
    final copy = List.of(numbers);
    copy.removeRange(0, i);
    final nolid =
        copy.fold<int>(0, (previousValue, element) => previousValue + element);
    if (numbers[i] >= nolid - numbers[i]) {
      leader.add(numbers[i]);
    }
  }
  if (leader.last == 0) {
    leader.removeLast();
  }
  return leader;
}

